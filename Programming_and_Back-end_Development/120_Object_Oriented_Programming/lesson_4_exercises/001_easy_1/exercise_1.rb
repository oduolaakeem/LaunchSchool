# Which of the following are objects in Ruby? If they are objects, how can 
# you find out what class they belong to?

# true                      ->    Object
# "hello"                   ->    Object
# [1, 2, 3, "happy days"]   ->    Object
# 142                       ->    Object

puts true.class
puts "hello".class
puts [1, 2, 3, "happy days"].class
puts 142.class