// Javascript coding styles

// Semicolons

// Use semicolons after every statement, except for statement ending with blocks

// bad
var number
number = 5
number = number + 1

// good
var number;
number = 5;
number = number + 1;

// bad
while (number > 0) {
    number--;
};

// good
while (number > 0) {
    number--;
}
