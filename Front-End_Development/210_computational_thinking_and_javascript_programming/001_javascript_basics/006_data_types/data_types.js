/* Data types

  - number
  - boolean
  - string
  - null
  - undefined

  and one compound data type
    
  - object
  
*/

typeof 1.2;         // "number"
typeof 'hello';     // "string"
typeof false;       // "boolean"

// Numbers

// 64 bits numbers maximum

0.1 + 0.2; // returns 0.30000000000000004, not 0.3!

2 / 0;            // NaN
Math.sqrt(-1);    // Nan

// Boolean

2 > 1;    // true
0 == 0;   // true
1 > 3;    // false

// Strings

'Hello' + ', World'; // "Hello, World"



