// Variables

// single declaration
var myVariable;

// multiples declaration
var myVariable;
var otherVariable;
var anotherVariable;

// variable assignment

var number;

number = 3;   // variable "number" is assigned with value 3.

// single declarations with initialization
var myVariable = 'Hello, World!';

// multiple declaration and initialization
var myVariable = 'Hello,  World!';
var otherVariable = 23;
var anotherVariable = true;

// a variable that is declared but not assigned will be initialized to the value undefined

var foo;
foo;    //undefined

// Dynamic Typing

// initialize to a string
var myVariable = 'Hello, World!';

// re-assign to a number
myVariable = 23;

// now the variable holds a number value
myVariable;     // 23

